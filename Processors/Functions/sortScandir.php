	Input Params :
	--------------
		- $scan	[Array]	:	Tableau à trier
	
	Output Params :
	---------------
		- $scanSorted	[Array]	:	Tableau trié

/** -------------------------------------------------------------------------------------------------------------------- **
/** -------------------------------------------------------------------------------------------------------------------- **/
function sortScandir($scan){
	/** > Convertion en Array Associatif **/
	$assoc = Array();

	for($i = 0; $i < count($scan); $i++){
		$assoc[strtolower($scan[$i])] = $i;
	}
	
	/** > Triage du tableau **/
	ksort($assoc);
		
	/** > Construction du $scan trié à l'aide de $assoc **/
	foreach($assoc as $key){
		$scanSorted[] = $scan[$key];
	}
	
	/** > Return le tableau trié **/
	return $scanSorted;
}

